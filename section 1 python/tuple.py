#tuple
# Example tuple of numbers
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple)
# Example tuple of strings
colors = ("red", "green", "blue")
print(colors)
# Example tuple with mixed data types
mixed_tuple = ("world", 456, False)
print(mixed_tuple)
# Accessing elements by index
print(my_tuple[0]) # Output: 1
print(colors[2]) # Output: blue
# Tuples are immutable, so you cannot change elements after creation
