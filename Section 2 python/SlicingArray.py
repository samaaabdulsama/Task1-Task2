x = ['a', 'b' ,'c','d','e', 'f']
#print all elements 
print(x[:])
#slice elements from index 2 to index 5
print(x[2:5]) 
#slice from index 3 to the end of array
print(x[3:])
#slice from the beginning to the index 5
print(x[:5])
#slice from index 4 from the end to the index 1 from the end
print(x[-4:-1])